  <h2>Summertime</h2>
  <p>Most people flock to the Alps for the glorious powder, but we believe that the mountains are even more beautiful in the summer. Think waterfalls, rocky hikes, mountain biking, jumping streams, swimming in lakes and relaxing in the sun. What more could you want?<br><br>
  For an action-packed summer, Chalet Bronziers provides links for:
  <ul>
    <li>White water rafting</li>
    <li>Mountain biking</li>
    <li>Hiking trails</li>
    <li>Stand-up paddle boarding</li>
    <li>Swimming (lakes & pools)</li>
    <li>Paragliding</li>
  </ul>
</p>
<h2>Wintertime</h2>
<p>Peisey Nancroix and Paradiski is the obvious choice for a winter holiday. 
With long-stretching pistes suiting a variety of difficulties, there is always something to do. Staying at Chalet Bronziers gives you access to:
<ul>
    <li>264 pistes</li>
    <li>Ski and snowboard hire</li>
    <li>Sledging</li>
    <li>Cross-country skiing</li>
    <li>Nature walks</li>
    <li>Ski paragliding</li>
    <li>Heliskiing</li>
    <li>Bobsleigh</li>
    <li>Ice climbing</li>
</ul>
Not into the active stuff? Here's how you can still enjoy the Alps this winter:
<ul>
    <li>Husky riding</li>
    <li>Spa facilities across resort</li>
    <li>Bars, restaurants and nightlife</li>
    <li>Igloo experience</li>
</ul>
</p>
